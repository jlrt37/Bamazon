	item_id INTEGER(11) NOT NULL,
	products VARCHAR(30) NOT NULL,
	department VARCHAR(20) NOT NULL,
	price DECIMAL(10,2) NOT NULL,
	stock INTEGER(11) NOT NULL,
	PRIMARY KEY (item_id)
);

